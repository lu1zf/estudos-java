class ExemploCasting {
  
  public static void main(String[] args){
    
    double d3 = 3.14;
    int i = (int) d3; // foi feito o casting para que o valor de d3 se tornasse inteiro
    
    System.out.println(i);
    
    long x = 10000;
    int j = (int) x;
    
    System.out.println(j);
    
  }  
  
}
